/**
 * @file state.hpp
 * @brief Глобальное состояние симуляции и воспроизведения.
 *
 * SimState — лёгкая структура-«сигнал», которую совместно читают
 * Control (запись) и главный цикл main.cpp / replay.cpp (чтение).
 * Намеренно реализована как POD-структура без методов,
 * чтобы минимизировать зависимости.
 */

#ifndef SIMSTATEINC
#define SIMSTATEINC

/**
 * @struct SimState
 * @brief Состояние выполнения симуляции / воспроизведения.
 *
 * Экземпляр создаётся в @c main() и передаётся по ссылке в Control,
 * который модифицирует поля через обработчики клавиш GLFW.
 * Главный цикл опрашивает поля каждый кадр.
 *
 * @par Управление (назначение клавиш из control.cpp):
 *  - **Space**        → pause (вкл/выкл)
 *  - **Escape**       → running = false (выход)
 *  - **+** / **-**    → увеличить / уменьшить timeScale (×1.5 / ÷1.5)
 *  - **Shift + колёсико** → изменение cameraMoveScale (в Control)
 */
typedef struct SimState {

    /**
     * @brief Флаг работы основного цикла.
     *
     * Когда @c false, главный цикл завершается, симуляция останавливается
     * и приложение завершает работу. Устанавливается при нажатии Escape
     * или закрытии окна.
     */
    bool running = true;

    /**
     * @brief Флаг паузы симуляции.
     *
     * Когда @c true, шаги интеграции не выполняются (accumulator не растёт),
     * но рендеринг и управление камерой продолжают работать.
     * Переключается по Space.
     */
    bool pause = false;

    /**
     * @brief Масштаб симуляционного времени.
     *
     * Реальное время рендер-кадра умножается на @c timeScale перед добавлением
     * к аккумулятору шагов интеграции:
     * @code
     *   accumulator += realFrameTime * timeScale;
     * @endcode
     *  - 1.0  — реальное время
     *  - > 1  — ускорение (симуляция быстрее реального времени)
     *  - < 1  — замедление
     *  - 0    — пауза без использования флага pause
     *
     * В режиме Replay определяет скорость воспроизведения:
     * @code
     *   playbackTime += realDt * timeScale;
     * @endcode
     */
    float timeScale = 1.0f;

    /**
     * @brief Время реального кадра [секунды].
     *
     * Разница между текущим и предыдущим вызовом glfwGetTime().
     * Используется Control для интерполяции скорости движения камеры.
     */
    float deltaTime = 0.0f;

    /**
     * @brief Время предыдущего кадра [секунды, от glfwGetTime()].
     *
     * Хранится для вычисления @c deltaTime на следующей итерации.
     * @deprecated Фактически не используется в текущей версии main.cpp
     *             (lastTime хранится как отдельная локальная переменная).
     */
    float lastFrame = 0.0f;

} SimState;

#endif // SIMSTATEINC