/**
 * @file bodysystem.hpp
 * @brief Агрегатные параметры системы тел и перенос в систему центра масс.
 *
 * BodySystem вычисляет интегральные характеристики N-body системы:
 * суммарную массу, центр масс и скорость центра масс. Затем выполняет
 * «буст» — вычитает скорость центра масс из всех тел и переносит начало
 * координат в центр масс.
 *
 * Это стандартная операция перед началом симуляции, необходимая для того,
 * чтобы система не «уплывала» со временем из-за ненулевого суммарного импульса.
 */

#pragma once
#ifndef SYSTEMINC
#define SYSTEMINC

#include <glm/glm.hpp>
#include <vector>
#include "object.hpp"

/**
 * @class BodySystem
 * @brief Вычисляет параметры системы тел и переводит её в систему центра масс.
 *
 * Используется один раз перед стартом главного цикла симуляции.
 *
 * @par Пример:
 * @code
 * BodySystem sys(objs);
 * std::cout << "Центр масс: " << sys.getCenter() << "\n";
 * std::cout << "Полная масса: " << sys.getMass() << " M☉\n";
 * sys.transPointToSystem(objs);   // переносим в СЦМ
 * // Теперь центр масс системы совпадает с началом координат,
 * // суммарный импульс равен нулю.
 * @endcode
 */
class BodySystem {
public:
    /**
     * @brief Конструктор. Вычисляет параметры системы при создании.
     *
     * Вызывает внутренний метод calcParams() для подсчёта суммарной массы,
     * центра масс и скорости центра масс.
     *
     * @param objects Массив тел симуляции.
     */
    explicit BodySystem(std::vector<Object>& objects);

    // ── Геттеры ──────────────────────────────────────────────────────────

    /**
     * @brief Возвращает суммарную массу системы.
     * @return Σ mᵢ [M☉].
     */
    double getMass();

    /**
     * @brief Возвращает количество тел в системе.
     * @return Число тел N.
     */
    std::size_t getBodiesAmount();

    /**
     * @brief Возвращает скорость центра масс системы.
     *
     * v_cm = Σ(mᵢ · vᵢ) / Σmᵢ  [AU/год].
     * Должна стать нулевой после вызова transPointToSystem().
     *
     * @return Вектор скорости центра масс [AU/год].
     */
    glm::dvec3 getVel();

    /**
     * @brief Возвращает положение центра масс системы.
     *
     * r_cm = Σ(mᵢ · rᵢ) / Σmᵢ  [AU].
     * Используется для перехода в систему отсчёта центра масс.
     *
     * @return Вектор положения центра масс [AU].
     */
    glm::dvec3 getCenter();

    // ── Трансформация ─────────────────────────────────────────────────────

    /**
     * @brief Переносит систему тел в систему отсчёта центра масс.
     *
     * Выполняет две операции для каждого тела:
     *  1. **Пространственный перенос**: rᵢ -= r_cm
     *     (начало координат в центре масс).
     *  2. **Импульсный буст**: vᵢ -= v_cm
     *     (суммарный импульс системы становится равным нулю).
     *
     * После вызова система не будет дрейфовать в пространстве,
     * что упрощает визуализацию и анализ результатов.
     *
     * @param objects Массив тел (модифицируется in-place).
     */
    void transPointToSystem(std::vector<Object>& objects);

private:
    std::size_t bodiesAmount; ///< Количество тел в системе.
    glm::dvec3  systemVel;    ///< Скорость центра масс [AU/год].
    glm::dvec3  massCenter;   ///< Положение центра масс [AU].
    double      totalMass;    ///< Суммарная масса Σmᵢ [M☉].

    /**
     * @brief Вычисляет суммарную массу, центр масс и скорость центра масс.
     *
     * Вызывается один раз из конструктора. Сложность O(N).
     *
     * @param objects Массив тел для анализа.
     */
    void calcParams(std::vector<Object>& objects);
};

#endif // SYSTEMINC
